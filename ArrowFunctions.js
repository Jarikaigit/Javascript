// **** Example 1 ****
// definition
function calculateSum(a, b) {
    return a + b;
}
// function call
const sum = calculateSum(1, 2);
console.log(sum);
// **** Example 2 ****
// function expressions
const sumCalculator = function(a, b) {
    return a + b;
};
const sum2 = sumCalculator(1, 2);
console.log(sum2);
// **** Example 3 ****
// arrow functions
const sumCalculator2 = (a, b) => a + b;
const sum3 = sumCalculator2(1, 2);
console.log(sum3);








